
public class LegalMove {
	private int xCoord;
	private int yCoord;
	
	public LegalMove(int x, int y){
		this.xCoord = x;
		this.yCoord = y;
	}
	
	public void setXCoord(int x){
		xCoord = x;
	}
	
	public int getXCoord(){
		return xCoord;
	}
	
	public void setYCoord(int y){
		yCoord = y;
	}
	
	public int getYCoord(){
		return yCoord;
	}
	

}
